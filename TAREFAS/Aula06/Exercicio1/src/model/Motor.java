package model;

public class Motor {
	private int qtdPis;
	private int potencia;
	public Motor() {
		super();
	}

	public int getQtdPis() {
		return qtdPis;
	}
	public final void setQtdPis(int qtdPis) {
		this.qtdPis = qtdPis;
	}
	public int getPotencia() {
		return potencia;
	}
	public final void setPotencia(int potencia) {
		this.potencia = potencia;
	}
	
	
	
	
}
